      private static boolean isValidEmail(String email) {
        // Email regex pattern
        String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
        Pattern pattern = Pattern.compile(emailRegex);
        Matcher matcher = pattern.matcher(email);
        return matcher.matches();
    }

    private static boolean isValidFullName(String fullName) {
        // Full name regex pattern (Only letters, at least one space)
        String fullNameRegex = "^[A-Za-z]+(\\s[A-Za-z]+)+$";
        Pattern pattern = Pattern.compile(fullNameRegex);
        Matcher matcher = pattern.matcher(fullName);
        return matcher.matches();
    }

    private static boolean isValidPassword(String password) {
        // Password regex pattern (at least 8 characters, at least one uppercase letter, one lowercase letter, and one digit)
        String passwordRegex = "^(?=.*[a-z])(?=.*[A-Z])(?=.*\\d)[a-zA-Z\\d]{8,}$";
        Pattern pattern = Pattern.compile(passwordRegex);
        Matcher matcher = pattern.matcher(password);
        return matcher.matches();
    }
        private static void insererDansTableInscription(String email, String fullName, String hashedPassword) {
        try (Connection connection = DatabaseConnection.connectToDatabase();) {
            String query = "INSERT INTO users (email, full_name, password) VALUES (?, ?, ?)";
            try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
                preparedStatement.setString(1, email);
                preparedStatement.setString(2, fullName);
                preparedStatement.setString(3, hashedPassword);
                preparedStatement.executeUpdate();
                JOptionPane.showMessageDialog(null, "Vous êtes inscrits avec Succès!");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error during database insertion: " + e.getMessage());
        }
    }
